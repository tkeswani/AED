    public void showGraph() {
        int count = 1;
        money = new DefaultCategoryDataset();
        int totalExpense = 0;
        
        for(Order order: system.getMasterOrder().getOrderList()) {
            if(!(order.getOrderItem().isEmpty())) {
                for(OrderItem oi: order.getOrderItem()) {
                    
                      totalExpense = (int) (totalExpense + (oi.getDrug().getMarketPrice()*oi.getQuantity()));
                      }
            }
        }
        String abc = String.valueOf(count);
        money.addValue(system.getTotalDonation(), "TOTAL FUNDING", abc);
        money.addValue(totalExpense, "TOTAL EXPENSES", abc);
        
        JFreeChart chartFactory = ChartFactory.createBarChart3D("GRAPHICAL REPRESENTATION OF FINANCES", "FUNDING/EXPENSE", "AMOUNT", money, PlotOrientation.VERTICAL, true, true, false);
        BarRenderer renderer = null;
        CategoryPlot plot = chartFactory.getCategoryPlot();
        renderer = new BarRenderer();
        ChartFrame frame = new ChartFrame("Bar Chart for Total Funding", chartFactory);
        frame.setVisible(true);
        frame.setSize(700, 320);
        
        
    }
    //Populating the Enterprise Admin Tab table
    public void populateEntAdminTable() {
        DefaultTableModel model = (DefaultTableModel)entAdminTable.getModel();
        
        model.setRowCount(0);
        if(!(system.getNetworkList().isEmpty())) {
        for(Network network:system.getNetworkList()) {
            for(Enterprise enterprise : network.getEntDirectory().getEnterpriseList()) {
                for(UserAccount account:enterprise.getUserAccountDirectory().getUserAccountList()) {
                Object[] row = new Object[3];
                row[0] = enterprise.getName();
                row[1] = network.getName();
                row[2] = account.getUsername();
                
                model.addRow(row);
                }
            }
        }
        }
    }
    
