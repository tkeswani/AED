    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        String name = nameTxtField.getText();
        int flag = 0;
        if(name.isEmpty()) {
          JOptionPane.showMessageDialog(this, "Enter The Name Of The City");
        }
        else if (!(name.matches("[a-zA-Z\\s]+"))) {
            JOptionPane.showMessageDialog(this, "Invalid Input");
            nameTxtField.setText("");
    }
        
        else {
        if(system.getNetworkList().isEmpty()) {
        Network network = system.addNetwork();
        network.setName(name);
        nameTxtField.setText("");
        populateNetworkTable();
        
        populateNetworkComboBox();
        
        populateEntTypeComboBox();
        }
        else {
            for(Network net : system.getNetworkList()) {
                if(net.getName().equalsIgnoreCase(name)) {
                    JOptionPane.showMessageDialog(this, "Branch Already Present At That Location"
                            + "!!!");
                    flag = 1;
                }
            }
            if(flag == 0) {
                Network network = system.addNetwork();
                network.setName(name);
                nameTxtField.setText("");
                JOptionPane.showMessageDialog(this, "Branch Location Added!!!");
                populateNetworkTable();
        
                populateNetworkComboBox();
        
                populateEntTypeComboBox();
            }
        }
        
        }   
    }//GEN-LAST:event_submitBtnActionPerformed

    private void submitEntBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitEntBtnActionPerformed
        // TODO add your handling code here:
            
        int flag =0 ;
        String entName = entNameTxtField.getText();
        Network network = (Network) networkComboBox.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) entTypeComboBox.getSelectedItem();
        if(network == null || type == null) {
            JOptionPane.showMessageDialog(this, "Invalid Input");
        }
        else if(!(entName.matches("[a-zA-Z\\s]+"))) {
            JOptionPane.showMessageDialog(this, "Invalid Entry");
        }
        else {
            if(!(network.getEntDirectory().getEnterpriseList().isEmpty())) {
            for(Enterprise ent : network.getEntDirectory().getEnterpriseList()) {
                if(ent.getName().equals(entName)) {
                    flag=1;
                }
            }
            }
            if(flag == 0) {
        Enterprise enterprise = network.getEntDirectory().addEnterprise(entName, type);
        
        JOptionPane.showMessageDialog(this, "Partner Added!!!");
        entNameTxtField.setText("");
        populateTable();
            
        
//        populateEntAdminTable();
        
        populateNetAdminComboBox();
            }
            else {
                JOptionPane.showMessageDialog(this, "Partner Already Associated With The NGO!!!");
            }
        }
    }//GEN-LAST:event_submitEntBtnActionPerformed

    public void populateEntAdminComboBox(Network network) {
        entAdminComboBox.removeAllItems();;
        
        if(!(network.getEntDirectory().getEnterpriseList().isEmpty())) {
        for(Enterprise enterprise:network.getEntDirectory().getEnterpriseList()) {
            entAdminComboBox.addItem(enterprise);
        }
        }
    }
    public void populateNetAdminComboBox() {
        netAdminComboBox.removeAllItems();
        
        if(!(system.getNetworkList().isEmpty())) {
        for(Network network:system.getNetworkList()) {
            netAdminComboBox.addItem(network);
        }
        }
    }
